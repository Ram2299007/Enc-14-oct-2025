package com.Appzia.enclosure.Model;

public class arrayData {
    String newData,reverseData;

    public arrayData(String newData, String reverseData) {
        this.newData = newData;
        this.reverseData = reverseData;
    }

    public String getNewData() {
        return newData;
    }

    public void setNewData(String newData) {
        this.newData = newData;
    }

    public String getReverseData() {
        return reverseData;
    }

    public void setReverseData(String reverseData) {
        this.reverseData = reverseData;
    }
}
